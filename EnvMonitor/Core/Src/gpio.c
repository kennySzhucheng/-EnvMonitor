/** Configure pins as
        * Analog
        * Input
        * Output
        * EVENT_OUT
        * EXTI
*/
void MX_GPIO_Init(void)
{

  GPIO_InitTypeDef GPIO_InitStruct = {0};

  /* GPIO Ports Clock Enable */
  __HAL_RCC_GPIOA_CLK_ENABLE();
  __HAL_RCC_GPIOB_CLK_ENABLE();

  /*--- 初始输出电平：全部拉低 ---*/
  HAL_GPIO_WritePin(GPIOA, DHT22_PIN, GPIO_PIN_SET);       /* DHT22总线空闲=高 */
  HAL_GPIO_WritePin(GPIOB, SR04_TRIG_PIN | LED_RED_PIN | LED_YELLOW_PIN
                          | LED_GREEN_PIN | BUZZER_PIN, GPIO_PIN_RESET);

  /*--- PA0: DHT22 数据线（开漏输出，模拟1-Wire双向） ---*/
  GPIO_InitStruct.Pin = DHT22_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_OD;
  GPIO_InitStruct.Pull = GPIO_PULLUP;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
  HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

  /*--- PA4/PA5/PA6: 按键输入（内部上拉，按下为低） ---*/
  GPIO_InitStruct.Pin = KEY1_PIN | KEY2_PIN | KEY3_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
  GPIO_InitStruct.Pull = GPIO_PULLUP;
  HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

  /*--- PB0: HC-SR04 Trig 推挽输出 ---*/
  GPIO_InitStruct.Pin = SR04_TRIG_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_PULLDOWN;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

  /*--- PB12/PB13/PB14: LED红黄绿 ---*/
  GPIO_InitStruct.Pin = LED_RED_PIN | LED_YELLOW_PIN | LED_GREEN_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

  /*--- PB15: 蜂鸣器 ---*/
  GPIO_InitStruct.Pin = BUZZER_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

}

